export function createSeedFilms(): Omit<Film, 'id' | 'ulid' | 'addedAt' | 'dateAdded' | 'updatedAt'>[] {
  return baseFilms.map((film) => ({
    ...film,
    images: film.images || [],
  }));
}

export const seededFilms: Film[] = baseFilms.map((film, index) => ({
  ...film,
  id: `film-${index + 1}`,
  ulid: randomUlid(),
  addedAt: nowMinusDays(index),
  dateAdded: nowMinusDays(index),
  updatedAt: nowMinusDays(Math.max(0, index - 2)),
  releaseYear: film.releaseYear,
  posterUrl: film.posterUrl || undefined,
  images: film.images || [],
}));
